#ifndef LANGUAGE_2_CODE_STYLE_SPECIFICATION_H
#define LANGUAGE_2_CODE_STYLE_SPECIFICATION_H

/**
 *  Ref : https://google.github.io/styleguide/cppguide.html
 *
 *  Naming Style :
 *      1.  The file name will use hungarian style.
 *      2.  The function name will use big-camel style, except get/set function which should use hungarian instead.
 *      3.  The type name (class, structure, enum, typedef/using) will use big-camel style.
 *      4.  The variable name will use hungarian style, class member should end with underline.
 *      5.  The constant or enum variable should start with k then use big-camel style.
 *
 * Include Style :
 *      1.  Most associate file
 *      2.  C standard file
 *      3.  C++ standard file
 *      4.  Other third-party library
 *      5.  Other file from the same project.
 */


#endif //LANGUAGE_2_CODE_STYLE_SPECIFICATION_H
